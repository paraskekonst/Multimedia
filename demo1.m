function newimg= dctquantandBack(image,table,qscale)
%xwrismos pinaka se 8x8 block
 [x, y]=size(image);
 x=x/8;  
 y=y/8;
 blocksimg = mat2cell(image, repelem(8, x), repelem(8, y));
 qblock=cell(x, y);
 idctblock=cell(x, y);
 % dct kai kvantismos
 for i=1:x
     for j=1:y
         dctblock=blockDCT(blocksimg{i,j});
         qblock{i,j}=quantizeJPEG(dctblock, table, qscale);
     end
 end
 %apokvantismos k iDCT
 for i=1:x
     for j=1:y
         dctblock = dequantizeJPEG( qblock{i,j}, table, qscale);
         idctblock{i,j}=round( iBlockDCT(dctblock));
     end
 end
newimg=cell2mat(idctblock);
end


