function img = checkImgSize(image)
%elengos gia to an oi diastaseis einai pollaplasio toy 8
[M, N , ~]=size(image);
rows=M-mod(M,8);
cols=N-mod(N,8); 
img=imresize(image,[rows cols]);
end


